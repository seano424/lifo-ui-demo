class UrgencyCalculator:
    """Calculates urgency scores for scanned items"""

    @staticmethod
    def calculate_urgency_score(expiry_date: date, confidence_score: float | None) -> tuple[float, list[str]]:
        """
        Calculate urgency based on expiry date and confidence

        Returns:
            Tuple of (urgency_score, warnings)
        """
        warnings = []
        days_to_expiry = (expiry_date - date.today()).days
        urgency_score = 0.1

        if days_to_expiry <= 0:
            urgency_score = 1.0
        elif days_to_expiry <= 1:
            urgency_score = 0.95
        elif days_to_expiry <= 3:
            urgency_score = 0.8
        elif days_to_expiry <= 7:
            urgency_score = 0.6

        # Validate expiry date confidence
        if confidence_score is not None and confidence_score < 0.8:
            warnings.append(
                f"Low confidence on expiry date ({confidence_score:.2f}) - please verify"
            )

        return urgency_score, warnings