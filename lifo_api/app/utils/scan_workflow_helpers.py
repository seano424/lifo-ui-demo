class ScanOutProcessor:
    """Main processor for scan-out workflow"""

    def __init__(self):
        self.quantity_validator = QuantityValidator()
        self.financial_calculator = FinancialImpactCalculator()
        self.action_creator = ActionDataCreator()
        self.effectiveness_calculator = ActionEffectivenessCalculator()
        self.realtime_trigger = RealtimeUpdateTrigger()

    async def process_scan_out(
        self,
        store_id: str,
        batch_id: str,
        scan_out_data: ScanOutRequest,
        batch_data: dict[str, Any],
        user_id: str,
    ) -> tuple[str, float, str, float, float, float, dict[str, Any]]:
        """
        Process scan-out workflow

        Returns:
            Tuple of (action_id, new_quantity, new_status, effectiveness_score, revenue_impact, waste_prevented, update_data)
        """
        # Validate quantity and calculate new status
        new_quantity, new_status = self.quantity_validator.validate_quantity(
            batch_data, scan_out_data
        )

        # Calculate financial impact
        revenue_impact, waste_prevented = self.financial_calculator.calculate_financial_impact(
            scan_out_data, batch_data
        )

        # Prepare action record
        action_data = self.action_creator.create_action_data(
            scan_out_data, batch_id, store_id, batch_data, revenue_impact, waste_prevented, user_id
        )

        # Calculate effectiveness score
        effectiveness_score = await self.effectiveness_calculator.calculate_action_effectiveness(
            action_data, batch_data, scan_out_data
        )

        # Prepare real-time update data
        update_data = {
            "action": scan_out_data.action.value,
            "new_quantity": new_quantity,
            "status": new_status,
            "effectiveness_score": effectiveness_score,
        }

        # Trigger real-time update (async, don't wait)
        await self.realtime_trigger.trigger_realtime_update(store_id, batch_id, update_data)

        return (
            action_data["action_id"],
            new_quantity,
            new_status,
            effectiveness_score,
            revenue_impact,
            waste_prevented,
            update_data,
        )


