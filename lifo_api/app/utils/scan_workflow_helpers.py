class ScanInProcessor:
    """Main processor for scan-in workflow"""

    def __init__(self):
        self.product_validator = ProductValidator()
        self.batch_generator = BatchNumberGenerator()
        self.scoring_calculator = ScoringCalculator()
        self.batch_creator = BatchDataCreator()

    async def process_scan_in(
        self,
        store_id: str,
        batch_data: ScanInRequest,
        read_ops,
        user_id: str,
    ) -> tuple[dict[str, Any], str, float, str, list[str], list[str]]:
        """
        Process scan-in workflow

        Returns:
            Tuple of (batch_creation_data, batch_number, initial_score, urgency_level, recommendations, warnings)
        """
        # Validate product exists or prepare for creation
        product_info = await self.product_validator.validate_or_prepare_product(
            batch_data.product_sku, store_id, read_ops
        )

        # Generate batch number if not provided
        if not batch_data.batch_number:
            batch_number = await self.batch_generator.generate_batch_number(
                store_id, batch_data.product_sku, batch_data.expiry_date
            )
        else:
            batch_number = sanitize_text_input(batch_data.batch_number)

        # Prepare batch data for creation
        batch_creation_data = self.batch_creator.create_batch_creation_data(
            batch_data, product_info, store_id, batch_number, user_id
        )

        # Calculate initial score for immediate feedback
        initial_score, urgency_level, recommendations, warnings = (
            self.scoring_calculator.calculate_initial_score(
                batch_data.expiry_date, batch_data.cost_price, batch_data.selling_price
            )
        )

        return (
            batch_creation_data,
            batch_number,
            initial_score,
            urgency_level,
            recommendations,
            warnings,
        )


class QuantityValidator:
    """Handles quantity validation for scan-out workflow"""

    @staticmethod
    def validate_quantity(
        batch_data: dict[str, Any], scan_out_data: ScanOutRequest
    ) -> tuple[float, str]:
        """
        Validate quantity and calculate new status

        Returns:
            Tuple of (new_quantity, new_status)
        """
        from app.models.scan_models import ScanWorkflowException

        current_quantity = batch_data["current_quantity"]
        if scan_out_data.quantity_moved > current_quantity:
            raise ScanWorkflowException(
                f"Cannot move {scan_out_data.quantity_moved} items - only {current_quantity} available",
                workflow="scan_out",
                error_code="INSUFFICIENT_QUANTITY",
                status_code=400,
            )

        # Calculate new quantity and status
        new_quantity = current_quantity - scan_out_data.quantity_moved
        new_status = "active" if new_quantity > 0 else "sold"

        if scan_out_data.action in ["discarded", "donated"]:
            if new_quantity == 0:
                new_status = scan_out_data.action.value

        return new_quantity, new_status


class FinancialImpactCalculator:
    """Handles financial impact calculations for scan-out workflow"""

    @staticmethod
    def calculate_financial_impact(
        scan_out_data: ScanOutRequest, batch_data: dict[str, Any]
    ) -> tuple[float, float]:
        """
        Calculate financial impact

        Returns:
            Tuple of (revenue_impact, waste_prevented)
        """
        revenue_impact = 0.0
        waste_prevented = 0.0

        if scan_out_data.action in ["sold_full_price", "sold_discounted"]:
            selling_price = (
                scan_out_data.actual_selling_price or batch_data["selling_price"]
            )
            revenue_impact = selling_price * scan_out_data.quantity_moved

            if scan_out_data.action == "sold_discounted":
                waste_prevented = revenue_impact  # Revenue recovered vs total loss

        elif scan_out_data.action == "donated":
            # Estimate waste prevented value (cost recovery + social impact)
            cost_value = batch_data["cost_price"] * scan_out_data.quantity_moved
            waste_prevented = cost_value * 0.7  # Estimated social value

        return revenue_impact, waste_prevented


